
public class Pointer {
    private int x;
    private int y;
    private int steps;
    private int[][] desk;

    public Pointer(int y, int x, int steps, int[][] desk) {
        this.x = x;
        this.y = y;
        this.steps = steps;
        this.desk = desk;
    }

    public getX() { return x; }
    public getY() { return y; }
    public getSteps() { return steps; }
    public getDesk() { return desk; }
}
